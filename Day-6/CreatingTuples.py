# creating tuple with one item, we must include coma at the end of value to represent it as a tuple
colors = ("black",);

print(colors)

print(type(colors)) # tuple

# if we don't append comma at the end, it is not considered as tuple
colors = ("black");

print(colors)

print(type(colors)) #string